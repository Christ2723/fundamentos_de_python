# Hacer un programa que al digitar una edad de una persona me diga si es mayor o menor de edad.

edad = int(input("Digite su edad: "))

if edad >= 18:
    print("Usted es mayor de edad: ")
else:
    print("Usted no es mayor de edad:")  

# Decir si un numero es positivo o negativo.

num = int(input("Digite un numero para saber si es (t/f)")) 
if num > 0:
    print("positivo")
else:
    print("negativo")    